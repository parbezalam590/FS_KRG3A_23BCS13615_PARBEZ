package com.example.demo.entity;

import jakarta.persistence.*;
//provide all functionalites of a jpa , is direacty got from jpa , jpa helps us interact with rempsitory.
import lombok.*;
//provides dependencies for lombok, which is used to reduce boilerplate code in Java classes.
@Entity
@Data
// @Data Lombok annotation hai jo automatically getters, setters, toString(), equals(), hashCode() aur constructor generate karta hai.
@NoArgsConstructor//create karta hai 
//generates a constructor with no parameters, which is useful for creating instances of the class without providing any initial values,its done at backend by lombok.
//it eill give space for all the parameters and we can set values later on.
@AllArgsConstructor//pass karta hai
//maps all fields of the class to the database table, allowing for easy persistence and retrieval of data, its done at backend by lombok.
public class Student {
    //primary key required hota hai, so we use @Id annotation to specify that the id field is the primary key of the Student entity.
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    //specifies that the value of the id field should be automatically generated by the database using an identity column, which is a common strategy for generating unique identifiers.
    private int id;
    private String name;
    private String email;
    private String course;
}
